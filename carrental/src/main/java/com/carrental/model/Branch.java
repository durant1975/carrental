package com.carrental.model;


import lombok.Data;

import javax.persistence.*;
import java.util.ArrayList;
import java.util.List;

@Data
@Entity( name="branch")
public class Branch {

    @Id
    @Column(name = "Branch_ID", unique = true, nullable = false)
    @GeneratedValue(strategy = GenerationType.IDENTITY)

    private Integer Branch_ID;
    @Column(name="Branch_Name",unique =true, nullable=false,length =100)
    private Integer Branch_Name;
    @Column(name="Address",unique =true, nullable=false,length =100)
    private Integer Address;
    @Column(name="Phone",unique =true, nullable=false,length =100)
    private Integer Phone;
    @OneToMany
    @JoinColumn (name= "Employee_ID", referencedColumnName="Branch_ID")
    List<Employee> employees = new ArrayList();


}































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































